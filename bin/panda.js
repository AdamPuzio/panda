#!/usr/bin/env node

const Panda = require('../')
const packageJson = require('../package.json')
const { Command, Option } = require('../src/wasp')
const program = new Command()

program
  .description('Panda Development Framework CLI')
  .version(packageJson.version, '-v, --version')
  .option('-g, --global', 'Global Test', false)
  // .option('-d, --debug [level]', 'Run in debug mode', false)
  .command('start', 'start all applications and services')
  .command('run [app]', 'run an application and all services')
  .command('install', 'install a Package')
  .command('uninstall', 'uninstall a Package')
  .command('create-command', 'create a new command')
  .command('create-model', 'create a new model')
  .command('create-project', 'create a new project')
  .command('create-service', 'create a new service')
  .command('user-setup', 'set up a new user')
  .command('info', 'provides basic information about the local instance and environment')
  .command('debug', 'debug')
  .command('update', 'panda')
  .command('play', 'playground', { hidden: true })
  .command('create-panda-command', 'panda', { hidden: true })
  .command('cpc', 'panda', { executableFile: 'panda-create-panda-command', hidden: true })
  .command('panda', 'panda', { hidden: true })
  .parse(process.argv)
